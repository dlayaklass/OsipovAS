# ПСЕВДОКОД функции foo:
# Начало функции foo(параметр n)
#   Если n > 0, то:
#     Сохранить n в стеке
#     Уменьшить n на 1
#     Вызвать foo(n-1) рекурсивно
#     Восстановить n из стека
#     Умножить результат на n
#     Вернуть результат
#   Иначе:
#     Вернуть 1
# Конец функции

# ОПИСАНИЕ АЛГОРИТМА:
# Данная функция вычисляет факториал числа.
# Если входной параметр <= 0, возвращается 1.
# Если входной параметр > 0, функция рекурсивно вызывает себя
# с параметром (n-1), затем умножает результат на n.

# ОТВЕТ НА ВОПРОС: Что будет получено при передаче функции числа 7?
# При передаче числа 7 функция вернет 7! (факториал 7) = 5040
# 
# Вычисление:
# foo(7) = 7 * foo(6)
# foo(6) = 6 * foo(5)
# foo(5) = 5 * foo(4)
# foo(4) = 4 * foo(3)
# foo(3) = 3 * foo(2)
# foo(2) = 2 * foo(1)
# foo(1) = 1 * foo(0)
# foo(0) = 1
# 
# foo(1) = 1 * 1 = 1
# foo(2) = 2 * 1 = 2
# foo(3) = 3 * 2 = 6
# foo(4) = 4 * 6 = 24
# foo(5) = 5 * 24 = 120
# foo(6) = 6 * 120 = 720
# foo(7) = 7 * 720 = 5040

# РЕАЛИЗАЦИЯ НА ЯВУ (R):

foo <- function(n) {
  if (n > 0) {
    # Рекурсивный вызов с уменьшенным параметром
    result <- foo(n - 1)
    # Умножение результата на текущее значение n
    return(n * result)
  } else {
    # Базовый случай: возвращаем 1
    return(1)
  }
}

