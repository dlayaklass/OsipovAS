# Лабораторная работа №4
# Задание 1 - Векторное программирование
# Предобработка данных

# Создание вектора
my_vector <- c(21, 18, 21, 19, 25, 20, 17, 17, 18, 22, 17, 18, 18, 19, 19, 27, 21, 20, 24, 17, 15, 24, 24, 29, 19, 14, 21, 17, 19, 18, 18, 20, 21, 21, 19, 17, 21, 13, 17, 13, 23, 15, 23, 24, 16, 17, 25, 24, 22)

# Вычисление среднего значения
mean_value <- mean(my_vector)

# Вычисление стандартного отклонения
sd_value <- sd(my_vector)

# Отбор наблюдений, которые отклоняются от среднего меньше, чем на одно стандартное отклонение
# Условие: |x - mean| < sd
# Это эквивалентно: mean - sd < x < mean + sd
my_vector2 <- my_vector[abs(my_vector - mean_value) < sd_value]

# Вывод результатов
cat("Исходный вектор my_vector:\n")
cat("Количество элементов:", length(my_vector), "\n")
cat("Среднее значение:", mean_value, "\n")
cat("Стандартное отклонение:", sd_value, "\n")
cat("\n")

cat("Новый вектор my_vector2 (отклонение < 1 стандартное отклонение):\n")
cat("Количество элементов:", length(my_vector2), "\n")
cat("Элементы:", my_vector2, "\n")
cat("\n")

cat("Проверка: исходный вектор не изменен\n")
cat("Длина исходного вектора:", length(my_vector), "\n")

